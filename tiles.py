# ── Sprite pixel data (16x16 grids) ─────────────────────────

def _px(rows: list[str]) -> list[tuple[int, int]]:
    """Parse '#'/space grid into coordinate list."""
    pts = []
    for y, row in enumerate(rows):
        for x, ch in enumerate(row):
            if ch == "#":
                pts.append((x, y))
    return pts


_SPRITES: dict[int, list[tuple[int, int]]] = {}


def _s(codepoint: int, rows: list[str]) -> None:
    _SPRITES[codepoint] = _px(rows)


