# ── Registration ─────────────────────────────────────────────

def _pixels_to_tile(pixels: list[tuple[int, int]], tile_h: int, tile_w: int) -> np.ndarray:
    """Convert a 16x16 pixel list to an RGBA ndarray at the tileset's tile size."""
    tile = np.zeros((tile_h, tile_w, 4), dtype=np.uint8)
    scale_x = tile_w / 16
    scale_y = tile_h / 16
    for px, py in pixels:
        sx = int(px * scale_x)
        sy = int(py * scale_y)
        ex = max(sx + 1, int((px + 1) * scale_x))
        ey = max(sy + 1, int((py + 1) * scale_y))
        tile[sy:ey, sx:ex] = (255, 255, 255, 255)
    return tile


def register_sprites(tileset) -> None:
    """Register all pixel-art sprites on the given tcod tileset."""
    ts = tileset.tile_shape  # (height, width)
    tile_h, tile_w = ts
    for codepoint, pixels in _SPRITES.items():
        arr = _pixels_to_tile(pixels, tile_h, tile_w)
        tileset.set_tile(codepoint, arr)
